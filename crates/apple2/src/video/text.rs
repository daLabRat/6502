/// Apple II text line addresses (non-linear memory layout).
fn text_line_addr(line: usize, page2: bool) -> u16 {
    let base: u16 = if page2 { 0x0800 } else { 0x0400 };
    let offsets: [u16; 24] = [
        0x000, 0x080, 0x100, 0x180, 0x200, 0x280, 0x300, 0x380,
        0x028, 0x0A8, 0x128, 0x1A8, 0x228, 0x2A8, 0x328, 0x3A8,
        0x050, 0x0D0, 0x150, 0x1D0, 0x250, 0x2D0, 0x350, 0x3D0,
    ];
    base + offsets[line]
}

/// Apple II green phosphor color.
const GREEN: u32 = 0x33FF33;
const BLACK: u32 = 0x000000;

/// Lo-res colors (16 colors).
static LORES_COLORS: [u32; 16] = [
    0x000000, // 0: black
    0xDD0033, // 1: magenta/red
    0x000099, // 2: dark blue
    0xDD22DD, // 3: purple
    0x007722, // 4: dark green
    0x555555, // 5: gray 1
    0x2222FF, // 6: medium blue
    0x6688FF, // 7: light blue
    0x885500, // 8: brown
    0xFF6600, // 9: orange
    0xAAAAAA, // 10: gray 2
    0xFF9988, // 11: pink
    0x11DD00, // 12: green
    0xFFFF00, // 13: yellow
    0x44FF99, // 14: aqua
    0xFFFFFF, // 15: white
];

/// Display width for bounds checking.
const WIDTH: u32 = 560;

/// Render 40x24 text mode (or 80x24 if col80 enabled).
pub fn render_text(fb: &mut FrameBuffer, memory: &Memory, switches: &SoftSwitches, flash_on: bool) {
    render_text_lines(fb, memory, switches, 0, 24, flash_on);
}

/// Render specific text lines (for mixed mode).
/// In 40-column mode, each character is 14px wide (doubled for 560px).
/// In 80-column mode, each character is 7px wide (80 × 7 = 560px).
pub fn render_text_lines(
    fb: &mut FrameBuffer,
    memory: &Memory,
    switches: &SoftSwitches,
    start_line: usize,
    end_line: usize,
    flash_on: bool,
) {
    if switches.col80 {
        render_text_lines_80col(fb, memory, switches, start_line, end_line, flash_on);
    } else {
        render_text_lines_40col(fb, memory, switches, start_line, end_line, flash_on);
    }
}

/// 40-column text: each char is 14px wide (7px doubled) for 560px total.
fn render_text_lines_40col(
    fb: &mut FrameBuffer,
    memory: &Memory,
    switches: &SoftSwitches,
    start_line: usize,
    end_line: usize,
    flash_on: bool,
) {
    for line in start_line..end_line {
        let addr = text_line_addr(line, switches.page2);
        for col in 0..40 {
            let ch = memory.ram[(addr + col) as usize];
            let is_inverse = ch < 0x40 || (ch < 0x80 && flash_on);
            let char_code = ch & 0x3F;

            for row in 0..8 {
                let y = line as u32 * 8 + row as u32;
                if y >= 192 { continue; }

                let pattern = get_char_pattern(char_code, row);

                for bit in 0..7 {
                    let x = col as u32 * 14 + bit * 2;
                    if x >= WIDTH { continue; }

                    let pixel_on = (pattern >> bit) & 1 != 0;
                    let lit = if is_inverse { !pixel_on } else { pixel_on };
                    let color = if lit { GREEN } else { BLACK };
                    fb.set_pixel_rgb(x, y, color);
                    if x + 1 < WIDTH {
                        fb.set_pixel_rgb(x + 1, y, color);
                    }
                }
            }
        }
    }
}

/// 80-column text: interleaved main/aux memory, 7px per character.
/// Even screen columns (0,2,4...) read from aux memory.
/// Odd screen columns (1,3,5...) read from main memory.
fn render_text_lines_80col(
    fb: &mut FrameBuffer,
    memory: &Memory,
    switches: &SoftSwitches,
    start_line: usize,
    end_line: usize,
    flash_on: bool,
) {
    for line in start_line..end_line {
        let addr = text_line_addr(line, switches.page2);
        for screen_col in 0..80u16 {
            // 80-col interleaving: even cols from aux, odd cols from main
            let mem_col = screen_col / 2;
            let ch = if screen_col & 1 == 0 {
                memory.read_aux_text(addr + mem_col)
            } else {
                memory.read_main_text(addr + mem_col)
            };

            let is_inverse = ch < 0x40 || (ch < 0x80 && flash_on);
            let char_code = ch & 0x3F;

            for row in 0..8 {
                let y = line as u32 * 8 + row as u32;
                if y >= 192 { continue; }

                let pattern = get_char_pattern(char_code, row);

                for bit in 0..7 {
                    let x = screen_col as u32 * 7 + bit;
                    if x >= WIDTH { continue; }

                    let pixel_on = (pattern >> bit) & 1 != 0;
                    let lit = if is_inverse { !pixel_on } else { pixel_on };
                    let color = if lit { GREEN } else { BLACK };
                    fb.set_pixel_rgb(x, y, color);
                }
            }
        }
    }
}

/// Render 40x48 Lo-Res graphics mode (doubled to 560px wide).
pub fn render_lores(fb: &mut FrameBuffer, memory: &Memory, switches: &SoftSwitches, flash_on: bool) {
    let max_lines = if switches.mixed_mode { 20 } else { 24 };

    for line in 0..max_lines {
        let addr = text_line_addr(line, switches.page2);
        for col in 0..40 {
            let byte = memory.ram[(addr + col) as usize];
            let top_color = LORES_COLORS[(byte & 0x0F) as usize];
            let bottom_color = LORES_COLORS[((byte >> 4) & 0x0F) as usize];

            for row in 0..4 {
                let y = line as u32 * 8 + row;
                if y >= 192 { continue; }
                for px in 0..14 {
                    let x = col as u32 * 14 + px;
                    if x < WIDTH {
                        fb.set_pixel_rgb(x, y, top_color);
                    }
                }
            }
            for row in 4..8 {
                let y = line as u32 * 8 + row;
                if y >= 192 { continue; }
                for px in 0..14 {
                    let x = col as u32 * 14 + px;
                    if x < WIDTH {
                        fb.set_pixel_rgb(x, y, bottom_color);
                    }
                }
            }
        }
    }

    // Mixed mode: text at bottom
    if switches.mixed_mode {
        render_text_lines(fb, memory, switches, 20, 24, flash_on);
    }
}

/// Get character pattern for a given character code and row.
/// CHAR_ROM is in ASCII order (space at index 0, '@' at index 32).
/// Apple II screen codes have '@' at 0 and space at 0x20.
/// XOR with 0x20 converts between the two orderings.
fn get_char_pattern(ch: u8, row: usize) -> u8 {
    let idx = ((ch ^ 0x20) & 0x3F) as usize;
    if idx < 64 && row < 8 {
        CHAR_ROM[idx][row]
    } else {
        0x00
    }
}
